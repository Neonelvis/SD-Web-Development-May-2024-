// Topic 1: What is JavaScript?


// 1.1 Introduction to JavaScript 

// What is JavaScript?

// JavaScript is a high-level, dynamic, and interpreted programming language primarily used for creating interactive web pages and web applications.

// It was initally developed by Brendan Eich in 1995 at Netscape Communication Corporation (now part of Mozilla).

// Despite its name, JavaScript is not related to Java Programming Language and has a different syntax and semantics.

// JavaScript is a client-side scripting language, which means that the code is executed in the user's web browser. However, with the introduction of Node.js, JavaScritp can also be used for server-side scripting, allowing developers to build more web applications using a single language.


// 1.2 How JavaScript works 
// JavaScript is an interpreted language, which means that the code is executed directly by a JavaScript engine without being compiled into machine code beforehand. 

// Web browsers include built-in Javascript engines, such as Googles V8 (used in Chrome and Node.js), Mozilla's SpiderMonkey (used in Firefox) and others 

// When a web page is loaded, the JavaScript engine creates an execution context for the script, which includes the global execution context and function execution contexts. The engine then follows the call stack to execute the line by line. JavaScript also uses an event loop to handle asynchronous operations, such as network, times and user interactions 


// 1.3 Adding JavaScript to a Web Page 
// There are three main ways to include JavaScript in an HTML document:

// a. Inline scripts: JavaScript code can be directly embedded within the <script> tags in the HTML file

// b. External scripts: Javascript code can be placed in a separate .js file and included in the HTML file using the <script> tag with the 'src' attribute pointing to the external file. 

// c. Event handlers: JavaScript code can be attached to HTML elements through event handlers, such onclick, onmouseover, etc 


// 1.4 JavaScript in Modern Web Development 
// JavaScript plays a crucial role in Modern Web Development, enabling the creation of dynamic and interactive websites and applications. 
// With the rise of Single-Page Applications (SPA's) and the increasing complexity of web applications, JavaScript has become an essential tool for building rich user interfaces and handling client-side logic. 

// JavaScript frameworks and libraries, such as React, Angular and Vue.js, have gained immense popularity, providing developers with tools and conventions to build scalable and maintanable applications.

// Additionally, JavaScript is increasingly being used in areas beyond web development, such mobile app development (through technologies like React Native), desktop application (with Electron), and even Internet of Things. 


// 1.5 JavaScript versions and Standards 

// JavaScript is standardised under the ECMAScript (ES) language specific, maintained by Ecma International Organisation. The latest stable version ECMAScript is ES2022 (ECMAScript2022), released in June 2022. 

// As new versions of ECMAScript are released, they introduce new syntax and features to the language. However, to ensure compatibility across different browsers and environments, modern JavaScript code often needs to be transpiled(converted) into a more widely support using tools like Babel. 

// The ECMAScript standard is developed and maintained by TC39 committe, which consists of representatives from various companies and organisations involved in web development. 

// TC39 is responsible for proposing, reviewing and approving new language features and improvements. 